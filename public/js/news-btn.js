var btn1=document.getElementById("news-btn1");

btn1.addEventListener("click", function() {
    // var val=btn.value;
    // var event_id="event"+val;
    var news1=document.getElementById("news1");
    var news_show1=document.getElementById("news1show");
    news1.style.display = "none";
    news_show1.style.display="block";
});

var coll1=document.getElementById("collapse-btn1");

coll1.addEventListener("click", function() {
    var news1=document.getElementById("news1");
    var news_show1=document.getElementById("news1show");
    news1.style.display = "block";
    news_show1.style.display="none";
});

